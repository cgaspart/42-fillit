int		ft_sqrt(int nb)
{
	int res;
	int i;

	res = 0;
	i = 0;
	if (nb <= 0)
		return (0);
	while (res != nb)
	{
		res = i * i;
		if (res > nb)
			return (i);
		if (i == nb)
		{
			return (0);
		}
		i++;
	}
	return (i - 1);
}
